package game.enums;
/**
 * Enum representing the four cardinal directions.
 * Used for movement and orientation in the game.<br><br>
 * Generally used by classes extending {@link city.cs.engine.Body Body}
 */
public enum Direction {
    /**
     * Represents the right direction.
     */
    RIGHT,
    /**
     * Represents the left direction.
     */
    LEFT,
    /**
     * Represents the up direction.
     */
    UP,
    /**
     * Represents the down direction.
     */
    DOWN
}
