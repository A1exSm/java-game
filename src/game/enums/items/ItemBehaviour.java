package game.enums.items;
/**
 * Enum representing different item behaviours in the game.
 */
public enum ItemBehaviour {
    /**
     * Represents an item which gets destroyed when used.
     */
    CONSUMABLE,
    /**
     * Represents an item which can be used for appearance.
     */
    COSMETIC,
    /**
     * Represents an item which gives the user a skill/perk
     */
    SKILL,
    /**
     * Represents an item which can be used as a weapon.
     */
    WEAPON
}
