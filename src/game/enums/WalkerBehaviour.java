package game.enums;
/**
 * Enum representing the types of walker behaviours
 */
public enum WalkerBehaviour {
    /**
     * The walker is passive and does not attack.
     */
    PASSIVE,
    /**
     * The walker is aggressive and attacks on sight.
     */
    AGGRESSIVE,
    /**
     * The walker is neutral and attacks only when provoked.
     */
    PATROLLER
}
